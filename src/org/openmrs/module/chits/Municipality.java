package org.openmrs.module.chits;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * Municipality code.
 */
@SuppressWarnings("serial")
public class Municipality implements Serializable {
	
	private Province province;
	
	private int municipalityCode;
	
	private String name;
	
	private String incomeClass;
	
	private String cityClass;
	
	private boolean urban;
	
	private boolean partiallyUrban;
	
	private boolean rural;
	
	private String district;
	
	private int registeredVoters;
	
	private int population;
	
	private int landArea;
	
	private List<Barangay> barangays = new ArrayList<Barangay>();
	
	/**
	 * @return the province
	 */
	public Province getProvince() {
		return province;
	}
	
	/**
	 * @param province the province to set
	 */
	public void setProvince(Province province) {
		this.province = province;
	}
	
	/**
	 * @return the municipalityCode
	 */
	public int getMunicipalityCode() {
		return municipalityCode;
	}
	
	/**
	 * @param municipalityCode the municipalityCode to set
	 */
	public void setMunicipalityCode(int municipalityCode) {
		this.municipalityCode = municipalityCode;
	}
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * @return the incomeClass
	 */
	public String getIncomeClass() {
		return incomeClass;
	}
	
	/**
	 * @param incomeClass the incomeClass to set
	 */
	public void setIncomeClass(String incomeClass) {
		this.incomeClass = incomeClass;
	}
	
	/**
	 * @return the cityClass
	 */
	public String getCityClass() {
		return cityClass;
	}
	
	/**
	 * @param cityClass the cityClass to set
	 */
	public void setCityClass(String cityClass) {
		this.cityClass = cityClass;
	}
	
	/**
	 * @return the urban
	 */
	public boolean isUrban() {
		return urban;
	}
	
	/**
	 * @param urban the urban to set
	 */
	public void setUrban(boolean urban) {
		this.urban = urban;
	}
	
	/**
	 * @return the partiallyUrban
	 */
	public boolean isPartiallyUrban() {
		return partiallyUrban;
	}
	
	/**
	 * @param partiallyUrban the partiallyUrban to set
	 */
	public void setPartiallyUrban(boolean partiallyUrban) {
		this.partiallyUrban = partiallyUrban;
	}
	
	/**
	 * @return the rural
	 */
	public boolean isRural() {
		return rural;
	}
	
	/**
	 * @param rural the rural to set
	 */
	public void setRural(boolean rural) {
		this.rural = rural;
	}
	
	/**
	 * @return the district
	 */
	public String getDistrict() {
		return district;
	}
	
	/**
	 * @param district the district to set
	 */
	public void setDistrict(String district) {
		this.district = district;
	}
	
	/**
	 * @return the registeredVoters
	 */
	public int getRegisteredVoters() {
		return registeredVoters;
	}
	
	/**
	 * @param registeredVoters the registeredVoters to set
	 */
	public void setRegisteredVoters(int registeredVoters) {
		this.registeredVoters = registeredVoters;
	}
	
	/**
	 * @return the population
	 */
	public int getPopulation() {
		return population;
	}
	
	/**
	 * @param population the population to set
	 */
	public void setPopulation(int population) {
		this.population = population;
	}
	
	/**
	 * @return the landArea
	 */
	public int getLandArea() {
		return landArea;
	}
	
	/**
	 * @param landArea the landArea to set
	 */
	public void setLandArea(int landArea) {
		this.landArea = landArea;
	}
	
	/**
	 * @return the barangays
	 */
	public List<Barangay> getBarangays() {
		return barangays;
	}
	
	/**
	 * @param barangays the barangays to set
	 */
	public void setBarangays(List<Barangay> barangays) {
		this.barangays = barangays;
	}
}
