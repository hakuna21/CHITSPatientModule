package org.openmrs.module.chits;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * Province code
 */
@SuppressWarnings("serial")
public class Province implements Serializable {
	
	private Region region;
	
	private int provinceCode;
	
	private String name;
	
	private String incomeClass;
	
	private String info;
	
	private int registeredVoters;
	
	private int population;
	
	List<Municipality> municipalities = new ArrayList<Municipality>();
	
	/**
	 * @return the region
	 */
	public Region getRegion() {
		return region;
	}
	
	/**
	 * @param region the region to set
	 */
	public void setRegion(Region region) {
		this.region = region;
	}
	
	/**
	 * @return the provinceCode
	 */
	public int getProvinceCode() {
		return provinceCode;
	}
	
	/**
	 * @param provinceCode the provinceCode to set
	 */
	public void setProvinceCode(int provinceCode) {
		this.provinceCode = provinceCode;
	}
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * @return the incomeClass
	 */
	public String getIncomeClass() {
		return incomeClass;
	}
	
	/**
	 * @param incomeClass the incomeClass to set
	 */
	public void setIncomeClass(String incomeClass) {
		this.incomeClass = incomeClass;
	}
	
	/**
	 * @return the info
	 */
	public String getInfo() {
		return info;
	}
	
	/**
	 * @param info the info to set
	 */
	public void setInfo(String info) {
		this.info = info;
	}
	
	/**
	 * @return the registeredVoters
	 */
	public int getRegisteredVoters() {
		return registeredVoters;
	}
	
	/**
	 * @param registeredVoters the registeredVoters to set
	 */
	public void setRegisteredVoters(int registeredVoters) {
		this.registeredVoters = registeredVoters;
	}
	
	/**
	 * @return the population
	 */
	public int getPopulation() {
		return population;
	}
	
	/**
	 * @param population the population to set
	 */
	public void setPopulation(int population) {
		this.population = population;
	}
	
	/**
	 * @return the municipalities
	 */
	public List<Municipality> getMunicipalities() {
		return municipalities;
	}
	
	/**
	 * @param municipalities the municipalities to set
	 */
	public void setMunicipalities(List<Municipality> municipalities) {
		this.municipalities = municipalities;
	}
}
